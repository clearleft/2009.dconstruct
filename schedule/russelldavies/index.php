			<h2><strong>Materialising and Dematerialising A Web of Data. (Or What We&rsquo;ve Learned From Printing The Internet Out)</strong></h2>
			
			<p>A couple of years back Tom Coates talked at dConstruct about &rsquo;Designing For A Web of Data&rsquo;, about the idea that your data, the bits that represent you and are useful or interesting to you on the web, are escaping the confines of particular websites and are getting smeared around the web through services and APIs and widgets and myriad other things. (I hope I&rsquo;m paraphrasing him fairly.) And then, last year, Matts Jones and Biddulph took that on more and talked about &rsquo;Designing For The Coral Reef&rsquo; and about how they were spreading Dopplr via that web of data, and onto devices, and asynchronous infrastructures and distributed interwoven systems, and slippy maps and geography, and there was a rubber duck in there somewhere as well.</p>

			<p>I loved all that. I wanted to make a contribution to that conversation. But my technical abilities are such that all I can really do to manipulate the web is print it out, and that didn't seem enough.</p>

			<p>And then I got involved with making <a href="http://www.reallyinterestinggroup.com/tofhwoti.html">Things Our Friends Have Written On The Internet</a> and I realised that printing it out is the next step. What's happening now is that the web of data wants to escape the screen, it wants to materialise into the real world, it wants to get physical, become objects. And that the next exciting stuff is going to be about designing data that can live on the screen, in devices, on paper, as things, wherever.</p>

			<p>So that's what I&rsquo;m hoping to talk about. About getting a little post-digital, about analogue friction, about printing to large industrial infrastructures, about unproducts and letter-boxes and rabbits. And there&rsquo;ll be jokes and silly videos too.</p>
